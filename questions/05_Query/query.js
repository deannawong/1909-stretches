function query(arr, obj) {
  let returnArr=[];

  const searchKeys=Object.keys(obj);

  arr.forEach(item=>{

    searchKeys.forEach(key=>{

      if(item[key]===obj[key]){
        if(!returnArr.includes(item)){
          returnArr.push(item);
        }
      }
    })
  })
  return returnArr;
}

